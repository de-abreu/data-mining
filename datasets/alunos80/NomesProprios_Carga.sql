ALTER TABLE Nomes ADD Id INTEGER;
SELECT Nome, Genero, Origem, row_number() OVER ()
    FROM Nomes;
UPDATE Nomes N
    SET ID=(
       SELECT Id FROM 
          (SELECT Nome, Genero, Origem, 
		          row_number() OVER (ORDER BY Nome, Genero, Origem) ID
               FROM Nomes) C
    WHERE C.Nome=N.Nome AND C.Genero=N.Genero AND C.Origem=N.Origem);


